	public void load() throws UnknownHostException, IOException,
			ClassNotFoundException {
		ArrayList<Suggest> suggest = new ArrayList<Suggest>();
		suggest = Client.check_allsuggest();
		if (suggest.size() != 0) {
			Object title[] = { "序号", "姓名", "电话", "情况", "时间" };
			Object detail[][] = new Object[suggest.size()][5];
			for (int i = 0; i < suggest.size(); i++) {
				detail[i][0] = Integer.toString(suggest.get(i).getId());
				detail[i][1] = suggest.get(i).getName();
				detail[i][2] = suggest.get(i).getPhone();
				detail[i][3] = suggest.get(i).getCondition();
				detail[i][4] = suggest.get(i).getCtime();
			}
			this.jTable1.setModel(new DefaultTableModel(detail, title));
		} else {
			JOptionPane.showMessageDialog(this, "您的快递还未到达");
		}

	}

