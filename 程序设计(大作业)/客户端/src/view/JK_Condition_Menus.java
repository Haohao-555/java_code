	private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {

		if (evt.getClickCount() == 1) {//对信息进行修改
			String condition = ((String) this.jTable1.getValueAt(this.jTable1
					.getSelectedRow(), 5));
			int id = Integer.valueOf(((String) this.jTable1.getValueAt(
					this.jTable1.getSelectedRow(), 0)));

			if (condition.equals("快递员已取件")) {
				JOptionPane.showMessageDialog(this, "快递已被收取了，不能修改信息");
			} else {
				
				
                new JK_Menus(user,id).setVisible(true);
                this.dispose();
			}
		}
	}

