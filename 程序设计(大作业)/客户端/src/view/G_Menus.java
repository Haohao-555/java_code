	public void load() throws Exception {
		ArrayList<Jworker> Jworker = new ArrayList<Jworker>();
		Jworker = Client.check_worker();
		Object title[] = { "序号", "姓名", "工号" };
		Object detail[][] = new Object[Jworker.size()][3];
		for (int i = 0; i < Jworker.size(); i++) {
			detail[i][0] = Integer.toString(Jworker.get(i).getId());
			detail[i][1] = Jworker.get(i).getName();
			detail[i][2] = Jworker.get(i).getNumber();
		}
		this.jTable1.setModel(new DefaultTableModel(detail, title));
	}

