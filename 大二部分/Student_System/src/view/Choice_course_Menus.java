	private void init() throws Exception {
		tea_ids.removeAll(tea_ids);
		co_ids.removeAll(co_ids);
		st_course = Student_Impl.select_couse();
		Object title[] = { "序号", "课程", "学分", "教师", "上课地点", "课程数量"};
		Object detail[][] = new Object[st_course.size()][6];
		for (int i = 0; i < st_course.size(); i++) {
			    co_ids.add(Integer.toString(st_course.get(i).getCo_id()));
				detail[i][0] = Integer.toString(i+1);
				detail[i][1] = st_course.get(i).getCourse();
				detail[i][2] = Integer.toString(st_course.get(i).getCredit());
				detail[i][3] = st_course.get(i).getName();
				detail[i][4] = st_course.get(i).getCo_time();
				detail[i][5] = st_course.get(i).getNumber();
				tea_ids.add(Integer.toString(st_course.get(i).getTea_id()));
		}
		defaultTableModel=new DefaultTableModel(detail, title);
		this.jTable1.setModel(defaultTableModel);
	}

