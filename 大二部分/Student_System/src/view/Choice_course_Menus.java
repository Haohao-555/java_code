	private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
		if (evt.getClickCount() == 1) {
			int id = Integer.valueOf(((String) this.jTable1.getValueAt(
					this.jTable1.getSelectedRow(), 0)));
			  System.out.println("课程id "+co_ids.get(id-1)+"教师id "+tea_ids.get(id-1));
			if (Student_Impl.iscourse(Integer.parseInt(co_ids.get(id-1)), stu.getName()) == true) {
				JOptionPane.showMessageDialog(this, "你已经选择了这门课");
			} else {
				try {
					Student_Impl.choice(stu.getId(), Integer.parseInt(co_ids.get(id-1)), Integer.parseInt(tea_ids.get(id - 1)));
					Student_Impl.course_1(Integer.parseInt(co_ids.get(id-1)));
					init();
					JOptionPane.showMessageDialog(this, "添加成功");
				} catch (Exception e) {
					// TODO Auto-generated catch block
					e.printStackTrace();
				}
			}
		}
	}

