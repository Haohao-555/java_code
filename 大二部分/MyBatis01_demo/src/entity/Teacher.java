package entity;

public class Teacher {
   private int id;
   private String teacher;
   private String pw;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getTeacher() {
	return teacher;
}
public void setTeacher(String teacher) {
	this.teacher = teacher;
}
public String getPw() {
	return pw;
}
public void setPw(String pw) {
	this.pw = pw;
}
   
}
