	public void load(String student, String number) {
		try {
			ArrayList<B> b=Student.check_grade(student);
			Object title[] = {"课程", "课程老师", "分数" };
			Object detail[][]= new Object[b.size()][3];
			for(int i=0;i<b.size();i++){
			    detail[i][0]=b.get(i).getCourse();
				detail[i][1]=b.get(i).getTeacher();
				detail[i][2]=b.get(i).getGrade();
			}
			this.jTable1.setModel(new DefaultTableModel(detail, title));
			jTable1.setEnabled(false);
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	}

